/**
 * PromptX JSON 合并(可选,环境变量)
 * 主人会从 PromptX 云端导出大老师的 JSON 角色设定放到环境变量
 * 这里 merge 进 hard-coded persona,让主人可以扩展或覆盖
 */
function mergePromptXPersona(hardCoded) {
  if (typeof process === 'undefined' || !process.env?.PROMPTX_PERSONA_JSON) {
    return hardCoded
  }
  try {
    const promptX = JSON.parse(process.env.PROMPTX_PERSONA_JSON)
    // 浅合并:把 promptX 拼到末尾作为"补充资料"
    return (
      hardCoded +
      '\n\n# PromptX 补充资料(主人从云端导出)\n' +
      '```json\n' +
      JSON.stringify(promptX, null, 2).slice(0, 8000) +
      '\n```'
    )
  } catch (e) {
    console.warn('[system.js] PROMPTX_PERSONA_JSON 解析失败,使用 hard-coded 人设:', e.message)
    return hardCoded
  }
}

export const SYSTEM_PROMPT = mergePromptXPersona(HARD_CODED_PERSONA)

/**
 * 拼接最终 system prompt
 * @param {string} personalityPrompt - 大老师人格补充 prompt
 * @param {string} stagePrompt - 当前阶段 prompt
 * @param {object} profile - 学生档案
 * @param {string} step - 当前步骤 ID
 * @returns {string}
 */
export function buildSystemPrompt(personalityPrompt, stagePrompt, profile, step) {
  const profileBlock = profile
    ? `\n\n# 你的学生档案
- 年级: ${profile.grade === 'primary' ? '小学(3-6 年级,9-12 岁)' : profile.grade === 'junior' ? '初中(初一-初三,12-15 岁)' : profile.grade === 'senior' ? '高中(高一-高三,15-18 岁)' : '未知'}
- 兴趣领域: ${(profile.interests || []).join('、') || '未填'}
- 科创经历: ${profile.experience || '未填'}
- 目标比赛: ${(profile.targetCompetitions || []).join('、') || '未填'}
- 是否有特别想解决的问题: ${profile.problem || '未填'}`
    : ''

  return [SYSTEM_PROMPT, personalityPrompt, stagePrompt].join('\n\n') + profileBlock
}

